function showContact() {
    if (document.getElementById("suiviPatient").style.visibility = "hidden") {
        document.getElementById("suiviPatient").style.visibility = "visible";
    } else{
       document.getElementById("suiviPatient").style.visibility = "hidden";
}
}


function showSuivi() {
    if (document.getElementById("suiviPatient").style.visibility === "hidden") {
        document.getElementById("suiviPatient").style.visibility = "visible";
    } else{
       document.getElementById("suiviPatient").style.visibility = "hidden";
}
}

